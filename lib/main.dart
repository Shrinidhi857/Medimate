class WiFiSocketScreen extends StatefulWidget {
  const WiFiSocketScreen({super.key});
  @override
  State<WiFiSocketScreen> createState() => _WiFiSocketScreenState();
}

class _WiFiSocketScreenState extends State<WiFiSocketScreen> {
  String message = "Ready to connect";
  Socket? socket;
  bool isConnecting = false;
  String? get userId => FirebaseAuth.instance.currentUser?.uid;
  String raspberryPiIP = "192.168.255.38";
  late int port=3000;


  @override
  void initState() {
    super.initState();
    // Don't auto-connect, let user initiate connection
  }

  Future<void> _sendUserIdToRaspberryPi() async {
    if (isConnecting) return;

    setState(() {
      message = "Connecting...";
      isConnecting = true;
    });

    try {
      final uid = userId ?? "unknown_user";
      print("ðŸ“¤ Attempting to connect to $raspberryPiIP:$port");

      try {
        final result = await InternetAddress.lookup(raspberryPiIP);
        if (result.isEmpty || result[0].rawAddress.isEmpty) {
          throw Exception("Cannot resolve Raspberry Pi IP address");
        }
        print("âœ… Network check passed. IP is reachable.");
      } catch (e) {
        throw Exception("Network error: Cannot reach Raspberry Pi ($e)");
      }

      print("Attempting socket connection...");
      socket = await Socket.connect(
        raspberryPiIP,
        port,
        timeout: const Duration(seconds: 100),
      );
      print("âœ… Socket connected successfully");
      setState(() => message = "Connected! Sending UID...");

      print("Sending UID: $uid");
      socket!.write(uid);
      await socket!.flush();
      print("âœ… UID sent successfully!");
      setState(() => message = "âœ… UID sent successfully!");

      socket!.listen(
            (data) {
          final response = utf8.decode(data);
          print("ðŸ“¥ Received from server: $response");
          setState(() => message = "âœ… Server response: $response");
        },
        onError: (error) {
          print("âŒ Socket error during listen: $error");
          setState(() => message = "âŒ Socket error: $error");
        },
        onDone: () {
          print("ðŸ“¡ Connection closed by server (listen)");
          setState(() => message = "Connection closed by server");
        },
      );

      Future.delayed(const Duration(seconds: 5), () {
        _closeConnection();
      });

    } catch (e) {
      print("âŒ Connection error: $e");
      setState(() => message = "âŒ Error: $e");
    } finally {
      setState(() => isConnecting = false);
    }
  }

  void _closeConnection() {
    try {
      if (socket != null) {
        socket!.close();
        socket = null;
        setState(() => message = "Connection closed");
      }
    } catch (e) {
      print("Error closing socket: $e");
    }
  }

  @override
  void dispose() {
    _closeConnection();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text("Connect to Dispenser"),
        leading: IconButton(
          icon: const Icon(Icons.arrow_back),
          onPressed: () => Navigator.of(context).pop(),
        ),
      ),
      body: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            Padding(
              padding: const EdgeInsets.all(16.0),
              child: Text(
                message,
                style: const TextStyle(fontSize: 18),
                textAlign: TextAlign.center,
              ),
            ),
            const SizedBox(height: 20),
            ElevatedButton(
              onPressed: isConnecting ? null : _sendUserIdToRaspberryPi,
              child: Text(isConnecting ? "Connecting..." : "Connect to Dispenser"),
            ),
            const SizedBox(height: 10),
            Text(
              "Raspberry Pi IP: $raspberryPiIP:$port",
              style: TextStyle(
                fontSize: 14,
                color: Colors.grey[600],
              ),
            ),
          ],
        ),
      ),
    );
  }
}